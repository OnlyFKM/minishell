#include "libft.h"

int	ft_isalnum(char x)
{
	if ((x >= 'a' && x <= 'z') || (x >= 'A' && x <= 'Z'))
		return (1);
	if (x >= '0' && x <= '9')
		return (1);
	if (x == '_' || x == '/' || x == '.' || x == ':' || x == '~' || x == ':')
		return (1);
	else
		return (0);
}
