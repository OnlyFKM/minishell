#include "../../inc/minishell.h"

int	ft_isspecial(char c)
{
	return (c == ' ' || c == '\"' || c == '\'' || c == '>' || c == '<'
		|| c == '|');
}

int	ft_isspace(int c)
{
	return (c == ' ');
}
